package com.yst.ovls.Util;


public class ErrorResult {

	private int code; //错误代码
	private String msg; //错误信息
	public int getCode() {
		return code;
	}
	public void setCode(int code) {

	    this.code = code;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	
}
